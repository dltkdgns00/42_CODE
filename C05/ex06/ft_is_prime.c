int	ft_is_prime(int nb)
{
	long	i;

	if (nb == 0 || nb == 1)
		return (0);
	i = 2;
	while (i * i <= nb)
	{
		if (i != nb && nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
