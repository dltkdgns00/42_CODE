#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}

int main()
{
	char dest[10];
	printf("%s",ft_strcpy(dest,"abcdefg"));
}
